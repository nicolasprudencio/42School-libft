#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		counter;
	int		i;
	char	*str;

	if (!s)
		return (NULL);
	counter = ft_strlen(s) + 1;
	str = malloc(counter * sizeof(char));
	if (!str)
		return (NULL);
	i = 0;
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
